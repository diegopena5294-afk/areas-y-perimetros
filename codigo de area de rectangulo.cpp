/*codigo de area de rectangulo*/
#include <iostream>
using namespace std;
int main()
{
int b=4;
int h=2;
int area=b*h;
int perimetro=2*(b+h);

    cout<<"el area del rectangulo es:"<<area;
    cout<<"el perimetro del rectangulo es:"<<perimetro;

    return 0;
}

